function sumar(){

	var contador = parseInt(document.getElementById("id").value);

	contador = contador + 1;
	
	document.getElementById("id").value = contador;

}

function restar(){

	var contador = parseInt(document.getElementById("id").value);

	contador = contador - 1;
	
	document.getElementById("id").value = contador;

}

